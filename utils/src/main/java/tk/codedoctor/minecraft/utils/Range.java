package tk.codedoctor.minecraft.utils;

/**
 * @author CodeDoctorDE
 */
public class Range {
    private int min;
    private int max;

    public Range(int min, int max) {
        this.min = min;
        this.max = max;
    }

    public int getMax() {
        return max;
    }

    public void setMax(int max) {
        this.max = max;
    }

    public int getMin() {
        return min;
    }

    public void setMin(int min) {
        this.min = min;
    }

    public boolean between(int number) {
        return number >= min && number <= max;
    }
}
